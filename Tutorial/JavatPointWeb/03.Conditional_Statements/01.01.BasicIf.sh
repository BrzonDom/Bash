#!/bin/bash

# Basic if Statements

# A basic if statement commands that if a particular condition is true, 
# then only execute a given set of actions. If it is not true, then do 
# not execute those actions. If statement is based on the following 
# format:

# if [ expression ];
#   then
#       statements
# fi


if [ true ]; then
    echo "Condition is True"

else
    echo "Condition is False"

fi
